package com.example.valueinjection;

public class Prices {
	
	String name;
	int prices;
	
	public Prices(){
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrices() {
		return prices;
	}

	public void setPrices(int prices) {
		this.prices = prices;
	}
	
	

}
